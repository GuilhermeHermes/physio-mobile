  void onAlterPicture(BuildContext context) async {
    final pickedFile = await picker.pickImage(source: ImageSource.gallery);
    if (pickedFile == null) {
      return null;
    } else {
      setState(() {
        _image = File(pickedFile.path);
        _imageWidget = Image.file(_image);
      });
    }
    ;
    if (pickedFile != null) {
      // Aqui você pode utilizar o caminho da imagem escolhida (pickedFile.path)
      print('Caminho da imagem escolhida: ${pickedFile.path}');
